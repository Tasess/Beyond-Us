                        <h1 class="border-bottom">Introduction</h1>
                        <br>
                        <br>
                        <p>Sitting back and watching the night sky with intense curiosity is a normal feeling. It is a feeling
                            that has existed for years and will lead to many more years of discovery and exploration. Our
                            universe is vast, so vast that it's hard to even imagine the distance between us and our closest
                            neighboring galaxy, Andromeda.
                            <mark>Andromeda is 2.537 million light years away</mark>, one light year is about 9,461,000,000,000
                            Km!
                            <br>
                            <br>Space is a Wonder worth learning about, even if it's just the basic concecpts.</p>
