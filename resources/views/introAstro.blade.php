                        <h1 class="border-bottom">Our Universe</h1>
                        <br>
                        <br>
                        <h4>Cosmic Address</h4>
                        <p>
                            We have out own "cosmic address" which is simply our place in our observable universe. Starting from the lowest level, we
                            live on Earth, in our solar system, in the Milk Way Galaxy, within the local group of the Virgo
                            Super Cluster that is in our observable universe.
                            <br>This helps build an understanding of perspective.</p>
                        <br>
                        <p>
                            Our Sun is one of many in a collection of stars that form into a disk-shape. This our Milk Way galaxy. A galaxy is almost
                            like an island of stars and most range from a few hundred million to more than a trillion; our
                            galaxy is rather large, containing more than 100 billion. This is already amazingly large, but
                            it doesn't even stop there.
                            <br>
                            <br> The local group we live in is a cluster of galaxies, the one we live in has about 40, with the
                            Milk Way being one of the largest. From observations, galaxies tend to have a void between them,
                            almost making a chain.
                            <br> Superclusters are what holds these clusters of galaxy clusters, meaning there is much more out
                            there.
                            <br>All of this makes up what is our Universe, all matter and energy included, and even the voids
                            between everything.
                        </p>
                        <h4>The Expanding Universe</h4>
                        <p>
                            Believe it or not,
                            <mark>our universe is expanding</mark>. By that, I mean that the average distance between galaxies
                            have been increasing with time. To get a better picture, think of raisin bread. As the bread
                            bakes, it will expand, but the raisins inside will not move, despite the distance between them
                            increasing. This is what is happening to our universe. While our galaxy is staying the same size,
                            we are moving further away from our neighbors.
                            <br>
                            <br> The origin of this expansion goes back and estimate of 14 billions years, the
                            <mark>Big Bang</mark>. What is occuring now is a much smaller scale as gravity forces matter together.
                            This is how galaxies and their clusters were formed, as gravity became stronger than the expansion.
                        </p>
                        <h4>Galactic Recycling</h4>
                        <p>
                            Stars and planets are formed when gravity makes clouds of dust and gas to condense together. Stars are not living, but they
                            still have their own "life cycle." When materials in a cloud gets compressed by gravity and becomes
                            dense and hot enough to do nuclear fusion, a star is born. As long as the star can do nuclear
                            fusion, it will "live" until the day it has exhausted all of its fuel. Smaller stars tend to
                            just blow it's content back into space, while massive stars supernovae, a massive explosion,
                            to send it's content back into space. This content that gets blown out mixes with other clouds
                            of dust and gas, and gravity will once again pull everything together to make new stars.
                            <br>
                            <br> Studying this cycle has taught us how deeply connected this is with our own existence. The early
                            universe contained the simplest chemical elements: hydrogen and helium, with very small traces
                            of lithium. We and Earth are primarily composed of carbon, nitrogen, oxygen, and iron. These
                            elements came to be due to stars' nuclear fusion, and their explosions after dying.
                        </p>
                        <h4>How do We Know?</h4>
                        <p>
                            Naturally, you would wonder how can claim to know anything when things happened so long ago. It's through light of distant
                            stars and galaxies. Light does take time to travel, and with how distant everything is we see
                            what things were previously, not as they are now. For example, when it comes to the moon, light
                            takes 1 second to get to us, and with the Sun, it is about 8 minutes. Light from distant stars
                            can take years, and this is why we measure distancec in light years. Please note,
                            <mark>Light years is a measure of distance, not time</mark>; It is the distance that light can travel
                            in a year.
                            <br> It is because light takes time to travel that we have learned that the farther away we look,
                            the further back we look in time. If Sirius, our closest star, died right now, it would take
                            8 minutes for us to even know about it.
                            <mark>When studying the universe, it is impossible to separate space and time.</mark>
                            <br>
                            <br>To put it to practice, the universe is about 14 billion years old. If we looked at a galaxy 7
                            billion light years away, we see the universe as it was when it was half its age. A galaxy 12
                            billion light years away, will appear as if the universe was only 2 billions years old. If we
                            tried to look past 14 billion light years, however, we will not see anything. This is where our
                            observable universe ends.
                            <mark>This is not limiting the size, the universe can be much larger, but we have no way of seeing
                                past this boundary.</mark>
                        </p>
