                        <h1 class="border-bottom">Galileo Galilei</h1>
                        <br>
                        <br>
                        <p>
                            Kepler's findings provided strong evidence for Copernicus' beliefs, but many scientist voiced objections to the the Copernican view, with three objections all having roots in the ancient Greeks.
                            <ul>
                                <li>
                                    Earth could not be moving because if it were, birds, falling stones, and clouds would be left behind as Earth moved.
                                </li>
                                <br>
                                <li>
                                    Noncircular objects contradict Aristotle's claim that the Heavens, Sun, Moon, planets, and stars, must be perfect and unchanging.
                                </li>
                                <br>
                                <li>
                                    At the time, no one detected stellar parallax, which is near impossible to detect with the naked eye, and has been one of the few things keeping the heliocentric model from being accepted by all since the beginning of Astronomy.
                                </li>
                            </ul>
                            Galileo almost single-handedly ruined the Aristotelian view of physics. He used experiments with rolling balls to demonstrate a moving object remains in motion unless a force acts to stop it (which is now remembered in Newton's first law of motion). This explained why objects sharing Earth's motion through space should stay with Earth, rather than fall behind.<br><br>
                            Tycho's observation of a supernova already disproved the belief that the Heavens were unchanging. Galileo disproved the heavenly perfection with his own telescope in the late 1609. With his telescope Galileo found sunspots on the Sun (considered imperfections at the time), along with discovering the mountains, craters, and valleys on the moon.<br><br>
                            Tycho did believe he saw stellar parallax, the issue is he thought the stars were much closer and much fewer. Galileo, with his telescope, could see the Milky Way dissolving into even more stars. He did not prove stellar parallax, but he made a good argument in its favor. It wasn't until Galileo made two of his earliest discoveries that the Earth-centered model was destroyed. His first was observing four moons clearly orbiting Jupiter, not Earth. His second discovery was observing Venus going through phases, proving Venus orbited the Sun.<br><br>
                            On June 22, 1633, Galileo was brought before the Church inquisition in Rome and ordered to keep his claims private. At the time, Galileo was 70, and feared for his life and agreed to keep his claims to himself. In 1757, church officials finally gave up the argument, removing Sun-centered works from their index of banned books, but never formally vindicated Galileo until 1992.
                        </p>

