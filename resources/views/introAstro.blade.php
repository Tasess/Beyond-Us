                        <h1 class="border-bottom">History of Astronomy</h1>
                        <br>
                        <br>
                        <h4>Ancient Civilizations and Astronomy</h4>
                        <p>
                            <mark>Most if not all ancient civilizations practiced astronomy.</mark> It was used to determine time
                            and mark seasons. People have used the Sun to determine time for ages. Many cultures would create
                            large obelisk to serve as clocks, working like a sundial. At night the position and phase of
                            the moon or the constellations visible would tell people what time it was.
                            <br>
                            <br>Structures like the Stonehenge helped determine the season. Templo Mayor in the Aztec city of
                            Tenochtitlán is one of the most marvelous structures. It had twin temples on a flat-top pyramid,
                            someone observing from the opposite side would see the Sun rise directly in between the temples
                            on the equinoxes. Many cultures had building with cardinal direction so that they could mark
                            the sunrise and sunset, and some would have buildings used to mark the Sun's position on special
                            dates.
                        </p>
                        <h4>The Greek</h4>
                        <p>
                            Most of modern science is linked back to the Greeks, this is because they developed three innovations still used today. The
                            first is understanding nature without relying on superstition, the second being the use of mathematics
                            to make sure ideas were precise, and the last being philosophy. Philosophy is not very scientific,
                            but it can help develop the skills necessary to make observations and reason. With these three
                            innovations they would create models of nature, something that we still do today.
                            <br>
                            <br> A common myth is that it was Columbus who learned the Earth was round, but information like
                            this was common knowledge by his time, and scholars even had a rough estimate of how large our
                            planet was. In about
                            <mark>240 B.C.E.</mark>, Eratosthenes managed to measure the Earth to be about 46,250 kilometers, not
                            to far off from what we consider the measurement to be (modern measurement of Earth is 40,070
                            kilometers). He did this based off the Sun's position during a Summer Solstice, studying the
                            shadow it caused to determine the radius based off the local well.
                            <br>
                            <br> In c. 260 B.C.E., Aristarchus brought forth the idea of a sun centered model, but it never gained
                            support. In
                            <mark>c. 500 B.C.E.</mark> Pythagoras announced his idea of the Earth being a sphere in the center
                            of the universe. Later, Aristotle observed and cited the curved shadow on the moon during a lunar
                            eclipse as more evidence for a spherical Earth. This lead to the creation of a geocentric model,
                            a model with Earth in the center. Of course, the greeks did not stop there, they continued to
                            make new models, each getting more complex as their theories developed. Eventually, Ptolomey's
                            model became one of the most well known, the
                            <mark>Ptolemaic model</mark>, which represented Greek's idea of "heavenly spheres" with Earth in the
                            center and all planets and an outer sphere held the stars.
                            <br>
                            <br>
                        </p>
                        <h4>Copernicus, Tycho, and Kepler</h4>
                        <p>
                            Fast-forward to 1473, a Polish Astronomer was about to change a few things.
                            <mark>Copernicus</mark>, knew about Aristarchus' ancient heliocentric (sun-centered) idea. He learned
                            to calculate each planet's orbital period around the Sun, and even the relative distance from
                            the Sun in terms of Earth-Sun distance, officially taking Aristarchus' theory well beyond what
                            it was. Despite being confident, he was worried of presenting his ideas, but it worked out when
                            scholars urged him to publish a book which later spread to many other scholars who were drawn
                            to the heliocentric theory. This is what is now known as the
                            <mark>Copernican Revolution.</mark>
                            <br>
                            <br> The main issue faced by most aastronomers is that there was a lack of data. Telescopes were
                            not invented at this point, not until
                            <mark>Tycho Brahe</mark>, an eccentric genius, provided one. He developed pre-telescopic instruments
                            and with this and an observatory built to study the sky, he compiled data and observations with
                            his assistant for three decades. This data collection was one of the best things to happen to
                            astronomy. The only issue is that Tycho never managed to make sense of his observations, and
                            on his deathbed begged Kepler, his apprentice, to succeed where he failed.
                            <br>
                            <br>
                            <mark>Kepler</mark> believed in the idea of perfectly circular orbits and did his best to match the
                            circular motions to Tycho's data. After years he finally found orbits matching Tycho's observations,
                            and eventually abandoned the idea of perfectly circular orbits. Kepler discovered that the planetary
                            orbits had to be ellipsis, if he were to take Tycho's data to heart, and he did. His decision
                            to abandon his own beliefs and to trust the data provided to him was a turning point in the history
                            of science in general.
                            <br>
                            <br> Kepler summarized his discovery into three simple laws still repeated today.
                            <ol>
                                <li>
                                    The orbit of each planet about the Sun is an
                                    <mark>ellipse</mark> with the sun at one
                                    <mark>focus</mark>.
                                    <br>
                                    <small>The Sun is off-centered and the planet's distance from the Sun varies during its orbit.</small>
                                </li>
                                <br>
                                <li>
                                    As a planet moves around its orbit, it sweeps out
                                    <mark>equal areas in equal times</mark>.
                                    <br>
                                    <small>Planets travel faster when closer to the Sun, slower when when its farther from the Sun.</small>
                                </li>
                                <br>
                                <li>
                                    More ditant planets orbit the Sun at slower average speeds, obeying the precise mathematical relationship
                                    <mark>
                                        <i>p</i>²=
                                        <i>a</i>³</mark>
                                    <br>
                                    <small>
                                        <mark>p</mark> is the planet's orbital period in years,
                                        <mark>a</mark> is its average distance from the sun in astronomical units.</small>
                                </li>
                            </ol>
                        </p>
